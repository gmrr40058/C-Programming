#include <stdio.h>

int main()
{
   int num1,num2;
    printf("Enter number one: ");
    scanf("%d",&num1);
    printf("Enter number two: ");
    scanf("%d",&num2);
    printf("Before swapping numbers\n number one= %d\n number two= %d\n",num1,num2);
    printf("After swapping numbers\n number one= %d\n number two= %d\n",num2,num1);
    return 0;
}
